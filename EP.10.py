#คำตอบที่ได้มีแค่ จริง กับ เท็จ

x=(5>10) #ค่า x = Boolean
y=(10==5) #ค่า y = Boolean

print("X : ",x)
print("Y : ",y)

#z = (5>10) and (10==5)
z = x and y
print("Z : ",z)

#อยากให้ z เป็น not เอา not ใส่หน้า z ได้เลย
print("Z' : ",not z)