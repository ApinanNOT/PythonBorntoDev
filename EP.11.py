#Compound Assignment

x=10
print("ก่อนเพิ่มค่า : ",x)

x=x+5 #เพิ่มค่าไปอีก 5 หรือเขียนได้เป็น x+=5
print("หลังเพิ่มค่า : ",x)

x=x-15 #ลบค่าลงไป 15 หรือเขียนได้เป็น x-=15
print("หลังลบค่า : ",x)

x=x*5 #นำค่าไปคูณ 5 หรือเขียนได้เป็น x*=5
print("หลังคูณค่า : ",x)

x=x/5 #นำค่าไปหาร 5 หรือเขียนได้เป็น x/=5
print("หลังหารค่า : %.2f"%x)

x=x**2 #นำค่าไปยกกำลัง 2 หรือเขียนได้เป็น x**=2
print("หลังยกกำลังค่า : ",x)

x=x%3 #นำค่าไป mod หารเอาเศษ หรือเขียนได้เป็น x%=3
print("หลัง mod ค่า : ",x)