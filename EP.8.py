x=5
y=10

print("ผลบวก : ",x+y)
print("ผลลบ : ",x-y)
print("ผลคูณ : ",x*y)
print("ผลหาร : ",x/y)
print("หารปัดเศษ : ",x//y)
print("ยกกำลัง : ",x**y)
print("หารเอาเศษ : ",x%y)

